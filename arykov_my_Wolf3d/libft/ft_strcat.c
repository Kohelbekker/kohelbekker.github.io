#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	s1_len;

	s1_len = ft_strlen(s1);
	ft_memcpy(&s1[s1_len], s2, ft_strlen(s2) + 1);
	return (s1);
}
