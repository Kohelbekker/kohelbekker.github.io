char	ft_if_print(char c)
{
	int a;

	if (c >= 65 && c <= 90)
		a = 0;
	else if (c >= 97 && c <= 122)
		a = 1;
	else if (c >= 48 && c <= 57)
		a = 2;
	else if ((c >= 33 && c <= 47) || (c >= 58 && c <= 64) ||
			(c >= 91 && c <= 96) || (c >= 123 && c <= 126))
		a = 3;
	else
		a = -1;
	return (a);
}
