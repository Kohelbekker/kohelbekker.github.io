#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	size_t	a;

	a = 0;
	if (!s1 || !s2)
		return (0);
	if (ft_strlen(s1) != ft_strlen(s2))
		return (0);
	while (a < ft_strlen(s1))
	{
		if (s1[a] != s2[a])
			return (0);
		a++;
	}
	return (1);
}
