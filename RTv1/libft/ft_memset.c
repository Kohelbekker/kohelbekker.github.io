#include "libft.h"

void	*ft_memset(void *dest, int c, size_t n)
{
	char	*p;
	size_t	i;

	p = dest;
	i = 0;
	while (i < n)
	{
		p[i] = c;
		i++;
	}
	return (dest);
}
