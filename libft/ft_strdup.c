#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*str2;
	int		i;

	i = ft_strlen(str);
	str2 = (char*)malloc(sizeof(char) * (i + 1));
	if (str2 == NULL)
		return (NULL);
	i = 0;
	while (str[i] != '\0')
	{
		str2[i] = str[i];
		i++;
	}
	str2[i] = '\0';
	return (str2);
}
