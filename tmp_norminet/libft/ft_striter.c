#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t	a;

	a = 0;
	if (!s || !f)
		return ;
	while (a < ft_strlen(s))
	{
		f(&s[a]);
		a++;
	}
}
