#include "libft.h"

int		ft_atoi(const char *str)
{
	int					a;
	unsigned long int	res;
	size_t				i;

	i = 0;
	a = 1;
	res = 0;
	while ((str[i] > 8 && str[i] < 14) || str[i] == 32)
		i++;
	if (str[i] == '-')
		a = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while ((str[i] != '\0') && (str[i] >= '0' && str[i] <= '9'))
	{
		if ((res > 9223372036854775807 || (res == 922337203685477580
			&& (str[i] - '0') > 7)) && a == 1)
			return (-1);
		else if ((res > 922337203685477580 || (res == 922337203685477580
				&& (str[i] - '0') > 8)) && a == -1)
			return (0);
		res = res * 10 + (str[i] - '0');
		i++;
	}
	return ((int)(res * a));
}
