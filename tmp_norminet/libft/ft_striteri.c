#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t	a;

	a = 0;
	if (!s || !f)
		return ;
	while (a < ft_strlen(s))
	{
		f(a, &s[a]);
		a++;
	}
}
